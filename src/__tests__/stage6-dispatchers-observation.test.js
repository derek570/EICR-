describe('dispatchDeleteObservation', () => {
  test('happy path: delete existing id → envelope ok:true, session array shrinks, perTurnWrites tracks', async () => {
    const session = makeSession();
    const logger = makeLogger();
    const perTurnWrites = createPerTurnWrites();

    // Seed: record first, then delete.
    const recordResult = await WRITE_DISPATCHERS.record_observation(
      {
        tool_call_id: 'tu_rec_1',
        name: 'record_observation',
        input: { text: 'Socket damage', code: 'C2', suggested_regulation: '651.2' },
      },
      makeCtx({ session, logger, perTurnWrites })
    );
    const id = JSON.parse(recordResult.content).observation_id;
    expect(session.extractedObservations.length).toBe(1);

    const delResult = await WRITE_DISPATCHERS.delete_observation(
      {
        tool_call_id: 'tu_del_1',
        name: 'delete_observation',
        input: { observation_id: id, reason: 'misheard' },
      },
      makeCtx({ session, logger, perTurnWrites })
    );

    expect(delResult.tool_use_id).toBe('tu_del_1');
    expect(delResult.is_error).toBe(false);
    const body = JSON.parse(delResult.content);
    expect(body.ok).toBe(true);
    expect(body.noop).toBeUndefined();

    // Session shrank.
    expect(session.extractedObservations.length).toBe(0);

    // Per-turn delete log.
    expect(perTurnWrites.deletedObservations.length).toBe(1);
    expect(perTurnWrites.deletedObservations[0]).toEqual({ id, reason: 'misheard' });

    // Two log rows total (one per dispatch); the delete row has outcome 'ok'.
    expect(logger.info).toHaveBeenCalledTimes(2);
    const delRow = logger.info.mock.calls[1][1];
    expect(delRow.tool).toBe('delete_observation');
    expect(delRow.outcome).toBe('ok');
    expect(delRow.is_error).toBe(false);
    expect(delRow.input_summary).toEqual({ observation_id: id, reason: 'misheard' });
  });

  test('reason propagates to perTurnWrites.deletedObservations', async () => {
    const session = makeSession();
    const logger = makeLogger();
    const perTurnWrites = createPerTurnWrites();

    const rec = await WRITE_DISPATCHERS.record_observation(
      {
        tool_call_id: 'tu_rec_2',
        name: 'record_observation',
        input: { text: 'Cable exposed', code: 'C1', suggested_regulation: '526.5' },
      },
      makeCtx({ session, logger, perTurnWrites })
    );
    const id = JSON.parse(rec.content).observation_id;

    await WRITE_DISPATCHERS.delete_observation(
      {
        tool_call_id: 'tu_del_2',
        name: 'delete_observation',
        input: { observation_id: id, reason: 'superseded' },
      },
      makeCtx({ session, logger, perTurnWrites })
    );

    expect(perTurnWrites.deletedObservations[0].reason).toBe('superseded');
  });

  test('BLOCK-2: delete-already-deleted is noop (is_error:false), NOT error', async () => {
    const session = makeSession();
    const logger = makeLogger();
    const perTurnWrites = createPerTurnWrites();

    // Shape-valid UUID that is NOT in session.extractedObservations.
    const absentId = '11111111-2222-4333-8444-555555555555';

    const result = await WRITE_DISPATCHERS.delete_observation(
      {
        tool_call_id: 'tu_del_absent',
        name: 'delete_observation',
        input: { observation_id: absentId, reason: 'misheard' },
      },
      makeCtx({ session, logger, perTurnWrites })
    );

    // Envelope: ok:true + noop:true + reason.
    expect(result.is_error).toBe(false);
    const body = JSON.parse(result.content);
    expect(body).toEqual({ ok: true, noop: true, reason: 'observation_not_found' });

    // Session UNCHANGED.
    expect(session.extractedObservations.length).toBe(0);
    // perTurnWrites UNCHANGED.
    expect(perTurnWrites.deletedObservations.length).toBe(0);

    // Log row: outcome 'noop', is_error false, validation_error null.
    const row = logger.info.mock.calls[0][1];
    expect(row.outcome).toBe('noop');
    expect(row.is_error).toBe(false);
    expect(row.validation_error).toBeNull();
    expect(row.input_summary).toEqual({ observation_id: absentId, reason: 'misheard' });
  });
});

