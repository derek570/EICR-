describe('Per-turn writes isolation across turns (Group 6 — BLOCK-1 cross-turn rule)', () => {
  test('two dispatches on different perTurnWrites instances: session persists; perTurnWrites does not', async () => {
    const session = makeSession();
    const logger = makeLogger();

    // Turn 1
    const turn1Writes = createPerTurnWrites();
    await WRITE_DISPATCHERS.record_observation(
      {
        tool_call_id: 'tu_t1',
        name: 'record_observation',
        input: { text: 'First observation', code: 'C3' },
      },
      { session, logger, turnId: 'sess-test-turn-1', perTurnWrites: turn1Writes, round: 1 }
    );

    // Turn 2 — fresh perTurnWrites.
    const turn2Writes = createPerTurnWrites();
    await WRITE_DISPATCHERS.record_observation(
      {
        tool_call_id: 'tu_t2',
        name: 'record_observation',
        input: { text: 'Second observation', code: 'C2' },
      },
      { session, logger, turnId: 'sess-test-turn-2', perTurnWrites: turn2Writes, round: 1 }
    );

    // Session sees BOTH (cross-turn persistence).
    expect(session.extractedObservations.length).toBe(2);
    expect(session.extractedObservations[0].text).toBe('First observation');
    expect(session.extractedObservations[1].text).toBe('Second observation');

    // perTurnWrites sees ONE each (reset-per-turn contract).
    expect(turn1Writes.observations.length).toBe(1);
    expect(turn2Writes.observations.length).toBe(1);
    expect(turn1Writes.observations[0].text).toBe('First observation');
    expect(turn2Writes.observations[0].text).toBe('Second observation');
  });
});

describe('Logger row shape (Group 7)', () => {
  test('record_observation log row has full canonical stage6_tool_call shape — no extras, no PII', async () => {
    const session = makeSession();
    const logger = makeLogger();
    const perTurnWrites = createPerTurnWrites();

    await WRITE_DISPATCHERS.record_observation(
      {
        tool_call_id: 'tu_shape',
        name: 'record_observation',
        input: { text: 'Fixture text should not appear in log', code: 'C3' },
      },
      makeCtx({ session, logger, perTurnWrites })
    );

    expect(logger.info).toHaveBeenCalledTimes(1);
    const [event, row] = logger.info.mock.calls[0];
    expect(event).toBe('stage6_tool_call');

    // Row shape: exactly these keys.
    expect(Object.keys(row).sort()).toEqual(
      [
        'input_summary',
        'is_error',
        'outcome',
        'phase',
        'round',
        'sessionId',
        'tool',
        'tool_use_id',
        'turnId',
        'validation_error',
      ].sort()
    );

    expect(row.sessionId).toBe('sess-test');
    expect(row.turnId).toBe('sess-test-turn-1');
    expect(row.tool_use_id).toBe('tu_shape');
    expect(row.tool).toBe('record_observation');
    expect(row.round).toBe(1);
    expect(row.phase).toBe(2);
    expect(row.is_error).toBe(false);
    expect(row.outcome).toBe('ok');
    expect(row.validation_error).toBeNull();

    // Fixture text never appears anywhere in the row.
    expect(JSON.stringify(row)).not.toContain('Fixture text should not appear in log');
  });
});

