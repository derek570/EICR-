export const CURRENT_VERSIONS = {
  beta_tester_agreement: '2026-05-12',
  cert_attestation_readings: '2026-05-12',
  cert_attestation_observations: '2026-05-12',
};

export const VALID_KINDS = Object.keys(CURRENT_VERSIONS);
export const VALID_ATTESTATION_KINDS = ['readings', 'observations'];

/**
 * Resolve a kind+version to its verbatim copy.
 *
 * Returns null if the kind isn't known or the version isn't in the
 * history for that kind. Callers should treat null as "I cannot honour
 * an acceptance against this version" — the validator path uses this
 * to reject spoofed-old-version submissions.
 */
export function getCopy(kind, version) {
  const history = KIND_HISTORY[kind];
  if (!history) return null;
  return history[version] || null;
}

/**
 * Is the version currently the active one for the kind?
 *
 * The /api/account/consent/accept route uses this to require new
 * acceptances against the latest version only (prevents users from
 * accepting a stale weaker draft). Per-attestation submissions on
 * /api/cert-attestations/accept apply the same rule.
 */
export function isCurrentVersion(kind, version) {
  return CURRENT_VERSIONS[kind] === version;
}

/**
 * Bundle every active version + copy for client consumption.
 *
 * Shape:
 *   {
 *     beta_tester_agreement: { version, copy: { heading, summary, ... } },
 *     cert_attestation_readings: { version, copy: { heading, body } },
 *     cert_attestation_observations: { version, copy: { heading, body } },
 *   }
 */
export function currentVersionsBundle() {
  const bundle = {};
  for (const kind of VALID_KINDS) {
    const version = CURRENT_VERSIONS[kind];
    bundle[kind] = {
      version,
      copy: getCopy(kind, version),
    };
  }
  return bundle;
}

/**
 * Cross-check that every entry in CURRENT_VERSIONS has a matching
 * KIND_HISTORY entry. Used by the unit test to catch the most likely
 * editor error: bumping CURRENT_VERSIONS without adding the new
 * version to KIND_HISTORY.
 */
export function isCoherent() {
  for (const kind of VALID_KINDS) {
    const v = CURRENT_VERSIONS[kind];
    if (!KIND_HISTORY[kind] || !KIND_HISTORY[kind][v]) return false;
  }
  return true;
}
