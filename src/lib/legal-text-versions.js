/**
 * Single source of truth for every piece of legal / contractual UI copy
 * that flows through the consent + attestation pipeline.
 *
 * Why centralised: the iOS app, the web app, and the backend validator
 * all need to agree on (a) what the current "active" version of each
 * piece of copy is, and (b) what the verbatim wording looks like for
 * any version a client claims to have accepted. Diverging the wording
 * between client and server would either reject lawful acceptances or,
 * worse, accept a forged acceptance against soft-but-claimed-old wording.
 *
 * Why JS-not-Markdown: this is UI copy that's rendered inside an
 * application chrome, not a long-form contractual document. The
 * contractual long-form lives at:
 *   - .planning/compliance/beta-tester-agreement.md
 *   - web/src/content/legal/public/beta-tester-agreement.md (synced copy
 *     served at certmate.uk/legal/beta-tester-agreement)
 * The consent screen and attestation sheets summarise those documents
 * and link out to the full text. Splitting them lets the in-app copy
 * be tuned (line breaks, length, accessibility) without re-publishing
 * the contractual document.
 *
 * Versioning: calendar versioning per kind (e.g. '2026-05-12'). A bump
 * is required whenever the meaning changes; cosmetic changes
 * (punctuation, capitalisation) do not require a bump but must be
 * called out in the commit. CURRENT_VERSIONS is the source of truth
 * for which version a new acceptance must point at. Old versions are
 * kept in KIND_HISTORY so historical reads ("what did I agree to on
 * 2026-03-04?") can still render the exact wording.
 *
 * Adding a new version (the runbook):
 *   1. Add the new version object to KIND_HISTORY[kind]
 *   2. Bump CURRENT_VERSIONS[kind] to point at the new version
 *   3. Run npm test — the version-coherence test will refuse if you
 *      forgot one of the two steps
 *   4. Ship via CI; the iOS app picks up the new wording on next
 *      app-start fetch of /api/legal/text-versions
 */

