/**
 * Stage 6 Phase 2 Plan 02-02 — Per-turn writes accumulator.
 *
 * WHAT: `createPerTurnWrites()` returns a fresh accumulator that the six write
 * dispatchers (Plans 02-03 + 02-04) push into as they mutate session state.
 * The Phase 2 event bundler (Plan 02-05) reads this accumulator AFTER the
 * tool-loop completes and translates its contents into the legacy `extraction`
 * result shape so iOS receives byte-identical traffic.
 *
 * WHY this is a factory (NOT a singleton on session): Research §Pattern 3 +
 * Pitfall 2. The accumulator resets per user turn. A singleton risks state
 * leaking from turn N into turn N+1's dedup/correction logic. The factory
 * contract forces the shadow harness (Plan 02-06) to new-up a fresh instance
 * every time it invokes `runToolLoop`.
 *
 * ---------------------------------------------------------------------------
 * SHAPE LOCK (MAJOR-1 — DO NOT MUTATE WITHOUT UPDATING PLAN 02-02)
 * ---------------------------------------------------------------------------
 * `readings` is a Map keyed by `${field}::${circuit}`. Its VALUES carry
 * {value, confidence, source_turn_id} — and NOTHING ELSE. Specifically,
 * `field` and `circuit` MUST NOT appear in the value object; they live in
 * the key and are reconstructed by splitting on '::' in Plan 02-05's bundler.
 *
 * Why: duplicating field/circuit in both the key and the value creates a
 * truth-divergence risk (which one does the bundler trust if they disagree?)
 * and silently breaks same-turn correction (the key is the correction
 * identity; if the value also carries an older copy, refactors drift).
 *
 * If a new Phase ever NEEDS more metadata in the value object, add it
 * alongside value/confidence/source_turn_id — but do NOT re-introduce
 * field/circuit. The tests in `stage6-per-turn-writes.test.js` assert this
 * shape and will fail if anyone re-adds those keys.
 * ---------------------------------------------------------------------------
 *
 * Slot semantics:
 *   readings            : Map<"${field}::${circuit}", {value, confidence, source_turn_id}>
 *                         Last-write-wins (same-turn correction path).
 *   cleared             : Array<{field, circuit, reason}>
 *                         Append-only. Order-preserving for bundler stability.
 *   observations        : Array<{id, code, location, text, circuit, suggested_regulation}>
 *                         Append-only. `id` generated by dispatcher in Plan 02-04.
 *   deletedObservations : Array<{id, reason}>
 *                         Append-only.
 *   circuitOps          : Array<{op: 'create'|'rename'|'update', circuit_ref, from_ref?, meta}>
 *                         Append-only. Phase 2 supports create/rename/update;
 *                         Phase 3+ may extend `op`.
 */

export function createPerTurnWrites() {
  return {
    readings: new Map(),
    cleared: [],
    observations: [],
    deletedObservations: [],
    circuitOps: [],
  };
}
