// value-normalise.js
//
// Single source of truth for normalising values that the EICR pipeline writes
// to the certificate. Three concerns sit here so iOS and the Node server can
// agree on identical semantics:
//
//   1. normaliseValue()   — equivalence test for "is this the same reading?"
//                           (used by filled-slots-filter and QuestionGate)
//   2. isValidSentinel()  — "N/A", "LIM", ">200" etc. ARE valid field values
//                           (the contract Sonnet's prompt teaches)
//   3. isEvasionMarker()  — "incomplete", "unknown", "not specified" etc.
//                           are model-evasion strings, NOT user-intended values
//
// CURRENT STATE (2026-04-27): iOS hand-mirrors these constants in
// Sources/Recording/DeepgramRecordingViewModel.swift's `nonValueStrings` Set.
// Drift between the two paths is the bug shape that produced the 2026-04-27
// Ivydene Road regression (iOS gate stripped "N/A" because its
// nonValueStrings set didn't match the backend prompt's "N/A is valid" rule).
//
// FOLLOW-UP (not yet implemented): codegen this set into a Swift file
// (Sources/Generated/Stage6ValueRules.swift) via a build-time script
// (scripts/generate-ios-value-rules.mjs) so future updates to
// STAGE6_VALUE_RULES below propagate to iOS automatically and a CI guard
// fails on `git status` drift. Until that script exists, ANY change to the
// rule sets here MUST be hand-mirrored on the Swift side. The
// `nonValueStrings` Set in DeepgramRecordingViewModel.swift carries a
// reciprocal pointer back to this file so the iOS author can find this
// context.

/**
 * Normalise a value so equivalent forms compare equal.
 *
 * Numeric inputs are parsed and reformatted via Number.toString so trailing
 * zeros ("0.130") collapse to the canonical form ("0.13"). Strings prefixed
 * with a number and optional unit suffix ("0.13 ohms", "0.13 Ω") reduce to
 * the same numeric form. Non-numeric strings (e.g. "LIM", ">200", "TT",
 * observation phrases) are lowercased and whitespace-collapsed but otherwise
 * preserved — their semantic meaning is the exact text.
 *
 * Returns "" for null / undefined / empty / whitespace-only input so callers
 * can early-return on empty.
 *
 * Intentionally conservative — does NOT:
 *   - round to a fixed decimal precision (Sonnet's extraction already
 *     preserves the inspector's stated precision; rounding could merge
 *     genuinely-different readings like 0.13 and 0.15 if we later widened
 *     the tolerance).
 *   - strip ">" or "<" prefixes (">200" is a >-flagged reading, not "200" —
 *     these have different meaning on an IR test).
 *   - handle units beyond stripping (e.g. "13 kΩ" vs "13000" won't match —
 *     mixing units inside a session is rare enough that supporting it
 *     would be more risk than benefit).
 *
 * @param {*} v
 * @returns {string} normalised string (may be empty)
 */
export function normaliseValue(v) {
  if (v === null || v === undefined) return '';
  const raw = String(v).trim().toLowerCase();
  if (!raw) return '';
  // Preserve semantic prefixes — these are NOT numeric equivalences.
  if (raw.startsWith('>') || raw.startsWith('<')) return raw;
  // Leading-number match: captures "0.13", "0.13 ohms", "0.13Ω", "-0.5mV".
  // If the non-numeric suffix is anything other than whitespace + a short
  // unit token, fall back to the raw string to avoid silently dropping
  // meaningful content.
  const m = raw.match(/^([-+]?\d*\.?\d+)(?:\s*([a-zμωΩµ]{0,4}))?$/iu);
  if (m) {
    const n = Number(m[1]);
    if (Number.isFinite(n)) return n.toString();
  }
  // Collapse internal whitespace for multi-word phrases (observation text).
  return raw.replace(/\s+/g, ' ');
}

// ---------------------------------------------------------------------------
// STAGE6_VALUE_RULES — shared sentinel + evasion classification
// ---------------------------------------------------------------------------
//
// These rules are exported as a frozen constants block so the Swift mirror
// (Sources/Generated/Stage6ValueRules.swift, generated by
//  scripts/generate-ios-value-rules.mjs) keeps the two sides in lockstep.
// CHANGES HERE require regenerating the Swift file.
//
// Design notes:
//
//   VALID_SENTINELS — values that the prompt teaches are LEGITIMATE for
//   certain fields. The user (or Sonnet on the user's behalf) can set these
//   intentionally. iOS MUST NOT strip them.
//
//   EVASION_MARKERS — strings Sonnet may emit when extraction failed and the
//   model wrote a placeholder instead of skipping. Stripping these is correct
//   defence-in-depth. They overlap with English-language prose that an
//   inspector might say in passing ("not specified by the customer"), but on
//   the (field, value) level the marker form is unambiguous because every
//   marker here is a complete, standalone token Sonnet would emit only when
//   it had nothing else to write.
//
// "N/A" is deliberately on the SENTINEL list, not the EVASION list. It is the
// cardinal historical bug-source: the iOS gate had it on its evasion list
// while the backend prompt had it on its sentinel list. The two paths now
// share THIS file (via codegen) so they cannot drift apart.

const VALID_SENTINELS = Object.freeze([
  'n/a',
  'na',
  'lim',
  '∞', // discontinuous continuity (BS 7671 reporting convention)
  'inf',
  'infinity',
]);

const EVASION_MARKERS = Object.freeze([
  'incomplete',
  'unknown',
  'partial',
  'unclear',
  'not specified',
  'not provided',
  'not given',
  'not stated',
  'pending',
  'tbd',
  'tbc',
]);

/**
 * Return true if the value is a recognised valid sentinel — i.e. the user
 * INTENTIONALLY set this field to a non-numeric marker that the certificate
 * schema considers a complete answer (N/A, LIM, ∞ for discontinuous).
 *
 * Case-insensitive. ">"-prefixed and "<"-prefixed numerics are NOT sentinels —
 * they're valid off-scale readings handled by their own rule (preserved by
 * normaliseValue()).
 *
 * @param {*} v
 * @returns {boolean}
 */
export function isValidSentinel(v) {
  if (v === null || v === undefined) return false;
  const raw = String(v).trim().toLowerCase();
  return VALID_SENTINELS.includes(raw);
}

/**
 * Return true if the value is a recognised evasion marker — i.e. Sonnet
 * emitted a placeholder string instead of a real value or a clean skip.
 * iOS callers strip these before writing to the job. Backend dispatchers
 * also gate on this in defence-in-depth checks.
 *
 * Case-insensitive.
 *
 * @param {*} v
 * @returns {boolean}
 */
export function isEvasionMarker(v) {
  if (v === null || v === undefined) return false;
  const raw = String(v).trim().toLowerCase();
  return EVASION_MARKERS.includes(raw);
}

/**
 * Composite gate: return true if the value should be ACCEPTED as a write,
 * false if it should be rejected. A value is accepted when it's non-empty
 * AND not an evasion marker. Sentinels (N/A, LIM, ∞) are explicitly accepted.
 *
 * This is the predicate iOS should use in `applySonnetReadings`. The Swift
 * mirror exposes the same gate as `Stage6ValueRules.acceptsAsWrite(value:)`.
 *
 * @param {*} v
 * @returns {boolean}
 */
export function acceptsAsWrite(v) {
  if (v === null || v === undefined) return false;
  const raw = String(v).trim();
  if (!raw) return false;
  if (isValidSentinel(raw)) return true;
  if (isEvasionMarker(raw)) return false;
  return true;
}

/**
 * Frozen exported snapshot of the sentinel + evasion sets. Used by the iOS
 * codegen script to emit Swift constants and by tests to assert membership
 * without exposing the internal arrays for mutation.
 */
export const STAGE6_VALUE_RULES = Object.freeze({
  VALID_SENTINELS,
  EVASION_MARKERS,
});
